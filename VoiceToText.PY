import speech_recognition as sr

# Initialize recognizer
recognizer = sr.Recognizer()

# Use microphone as source
with sr.Microphone() as source:
    print("üéôÔ∏è Adjusting for ambient noise... Please wait.")
    recognizer.adjust_for_ambient_noise(source)

    print("üé§ Speak now:")
    try:
        # Listen from mic
        audio_data = recognizer.listen(source, timeout=5)
        print("üîÅ Processing...")

        # Convert to text using Google Web Speech API
        text = recognizer.recognize_google(audio_data)
        print("\n‚úÖ Recognized Text:\n")
        print(text)

    except sr.UnknownValueError:
        print("‚ùå Could not understand your voice.")
    except sr.WaitTimeoutError:
        print("‚è≥ Listening timed out. Please try again.")
    except sr.RequestError as e:
        print(f"‚ö†Ô∏è Could not request results from Google Speech Recognition service; {e}")
